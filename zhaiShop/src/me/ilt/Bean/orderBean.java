package me.ilt.Bean;

import java.util.List;

public class orderBean {
private String id; //订单ID
private int userId; //用户ID
private double total; //订单总额
private int addressId; //收货地址ID
private String remarks;  //买家留言
private String time;  //下单时间
private int state;  //当前状态
private List<orderItemBean> itemList;//订单商品集合
public orderBean() {
	super();
}
public orderBean(String id, int userId, double total, int addressId,
		String remarks, String time, int state) {
	super();
	this.id = id;
	this.userId = userId;
	this.total = total;
	this.addressId = addressId;
	this.remarks = remarks;
	this.time = time;
	this.state = state;
}

public List<orderItemBean> getItemList() {
	return itemList;
}
public void setItemList(List<orderItemBean> itemList) {
	this.itemList = itemList;
}
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
public int getUserId() {
	return userId;
}
public void setUserId(int userId) {
	this.userId = userId;
}
public double getTotal() {
	return total;
}
public void setTotal(double total) {
	this.total = total;
}

public int getAddressId() {
	return addressId;
}
public void setAddressId(int addressId) {
	this.addressId = addressId;
}
public String getRemarks() {
	return remarks;
}
public void setRemarks(String remarks) {
	this.remarks = remarks;
}
public String getTime() {
	return time;
}
public void setTime(String time) {
	this.time = time;
}
public int getState() {
	return state;
}
public void setState(int state) {
	this.state = state;
}

}
